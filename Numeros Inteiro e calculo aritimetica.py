# Solicita ao usuário os três números inteiros
num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))
num3 = int(input("Digite o terceiro número inteiro: "))

# Realiza o cálculo da média aritmética
media = (num1 + num2 + num3) / 3

# Imprime o resultado para o usuário
print("A média aritmética é:", media)

##Explicação do código:
##
##Na primeira parte do código, o usuário é solicitado a digitar três números
##inteiros e cada um desses valores é armazenado em variáveis separadas (num1,
##num2 e num3).
##Em seguida, a média aritmética dos três números é calculada pela soma dos
##três números dividido por 3. O resultado é armazenado na variável media.
##Por fim, o resultado é impresso na tela utilizando a função print(),
##acompanhado da mensagem "A média aritmética é:".
